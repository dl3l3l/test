// SimpleJavaCode.java

public class SimpleJavaCode {

    public static void main(String[] args) {
        // 출력문 예제
        System.out.println("간단한 자바 코드입니다.");

        // 반복문 예제
        for (int i = 1; i <= 10; i++) {
            System.out.println("반복 횟수: " + i);
        }

        // 조건문 예제
        int number = 30;
        if (number > 0) {
            System.out.println("양수입니다.");
        } else if (number < 0) {
            System.out.println("음수입니다.");
        } else {
            System.out.println("0입니다.");
        }
    }
}
